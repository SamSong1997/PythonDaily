# 变量 = 数据的载体
# 1. 变量的名字可以用字母（unicode字符）、数字、下划线，不能使用特殊字符，数字不能开头
# 2. 变量名是区分大小写的（大小写敏感，x和X是两个不同的变量名）
# 3. 不能使用python中的关键字（python代码中有特殊含义的单词）和保留字（python使用过的名字）
# 4. 见名知意（看到变量的名字，就能知道它代表了什么意思）
# 5. 变量的命名使用全小写，多个单词用下划线连接（Snake case）（例如：student_age = 18）

